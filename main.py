import discord  #imports discord library
from discord.ext import commands
import os

client = commands.Bot(command_prefix="$")      #Start of creating cogs for the bot and fixed prefix for commanding the bot
@client.command()
async def load(ctx, extension):
    client.load_extension(f'cogs.{extension}')

@client.command()
async def unload(ctx, extension):
    client.unload_extension(f'cogs.{extension}')

@client.command()
async def reload(ctx, extension):
    client.unload_extension(f'cogs.{extension}')
    client.load_extension(f'cogs.{extension}')

for filename in os.listdir('./cogs'):
    if filename.endswith('.py'):
        client.load_extension(f'cogs.{filename[:-3]}')

client.run('!TOKEN')       #Runs the bot with the specified token